local Tunnel = module("vrp","lib/Tunnel")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
vSERVER = Tunnel.getInterface("codes")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CODES:DYNAMIC
-----------------------------------------------------------------------------------------------------------------------------------------
AddEventHandler("codes:Dynamic",function()
    local List = vSERVER.Codes()

    if List then
        exports.dynamic:AddMenu("Códigos","Gerenciar cupons de recompensa.","codes")
        exports.dynamic:AddButton(" Adicionar","Criar um novo código.","codes:Dynamic","","codes",true)

        for ID,v in pairs(List) do
            exports.dynamic:AddButton(v.Name,v.Info,"codes:Remove",ID,"codes",true)
        end
    end
end)